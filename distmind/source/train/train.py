def train(model, criterion, optimizer, epoch, args):
    # switch to train mode
    model.train()

    for i in range(100):
        if should_train():
            images = torch.randn(args.batch_size, 3, 224, 224)
            target = torch.LongTensor(args.batch_size).random_() % 1000

            if args.gpu is not None:
                images = images.cuda(args.gpu, non_blocking=True)
            if torch.cuda.is_available():
                target = target.cuda(args.gpu, non_blocking=True)

            # compute output
            output = model(images)
            loss = criterion(output, target)

            # compute gradient and do SGD step
            optimizer.zero_grad()
            loss.backward()
            optimizer.step()

            print ('Batch complete', i)
        else:
            time.sleep(0.001)

def save_checkpoint(state, is_best, filename='checkpoint.pth.tar'):
    torch.save(state, filename)
    if is_best:
        shutil.copyfile(filename, 'model_best.pth.tar')

def adjust_learning_rate(optimizer, epoch, args):
    """Sets the learning rate to the initial LR decayed by 10 every 30 epochs"""
    lr = args.lr * (0.1 ** (epoch // 30))
    for param_group in optimizer.param_groups:
        param_group['lr'] = lr


def accuracy(output, target, topk=(1,)):
    """Computes the accuracy over the k top predictions for the specified values of k"""
    with torch.no_grad():
        maxk = max(topk)
        batch_size = target.size(0)

        _, pred = output.topk(maxk, 1, True, True)
        pred = pred.t()
        correct = pred.eq(target.view(1, -1).expand_as(pred))

        res = []
        for k in topk:
            correct_k = correct[:k].view(-1).float().sum(0, keepdim=True)
            res.append(correct_k.mul_(100.0 / batch_size))
        return res


train_status = False
def thd_loop_func_listen_notification(ctrl_addr, ctrl_port, local_ip, local_port):
    global train_status
    train_status = False

    client = TcpClient(ctrl_addr, ctrl_port)
    server_id_b = struct.pack('II', local_ip, local_port)
    client.tcpSend(server_id_b)
    while True:
        model_name = client.tcpRecvWithLength()
        client.tcpSend(b'ACKN')
        train_status = ('train' in model_name)

def should_train():
    global train_status
    return train_status

